<h1 id="welcome">欢迎使用文档系统</h1>
<p>这是一个功能强大的在线文档系统，支持多级菜单、主题切换、多语言等功能。</p>

<h2 id="feature1">主要功能</h2>
<ul>
    <li>响应式设计，适配各种屏幕尺寸</li>
    <li>多级手风琴菜单（支持5级）</li>
    <li>主题切换（亮色/暗色）</li>
    <li>多语言支持</li>
    <li>文档内容搜索</li>
    <li>右侧标题导航</li>
    <li>优化的菜单样式和交互</li>
    <li>面包屑导航</li>
    <li>返回顶部按钮</li>
</ul>

<h2 id="feature2">快速开始</h2>
<p>要开始使用本系统，请按照以下步骤操作：</p>

<h3 id="step1">第一步：了解菜单结构</h3>
<p>左侧菜单支持无限层级，点击菜单项可以展开或收起子菜单。</p>

<h3 id="step2">第二步：切换文档分类</h3>
<p>顶部导航栏可以切换不同的文档分类，如指南、API、组件等。</p>

<h3 id="step3">第三步：使用搜索功能</h3>
<p>在顶部搜索框中输入关键词可以快速查找文档内容。</p>

<h2 id="feature3">高级功能</h2>
<p>本系统还提供了一些高级功能：</p>

<h3 id="adv1">主题定制</h3>
<p>点击右上角的主题切换按钮可以在亮色和暗色主题之间切换。</p>

<h3 id="adv2">多语言支持</h3>
<p>点击语言切换按钮可以选择不同的界面语言。</p>

<h3 id="adv3">用户信息</h3>
<p>点击用户头像可以查看和编辑个人信息。</p>

